// ========== Abstração ==========
/* 
- Processo mental que consiste em isolar um aspecto determinado de um estado de coisas relativamente complexo, a fim de simplificar a sua avaliação, classificação para permitir a comunicação com o mesmo
- Simplificar e generalizar cada vez mais

* Carro - veículo de duas rodas - veículo
*/

// ========== Herança ==========
/* 
- O objeto filho herda propriedades e métodos do objeto pai
*/

// ========== Encapsulamento ==========
/* 
- Cada classe tem propriedades e métodos independente do restante do código
*/

// ========== Polimorfismo ==========
/* 
- Objetos podem herdar a mesma classe pai, mas se comportarem de forma diferente quando invocamos seus métodos

* Falar - gato miando - cachorro latindo
*/
